namespace Maliev.CareerService.Data.Enums;

/// <summary>
/// Represents an employee's proficiency level in a skill (1-5 scale).
/// </summary>
public enum ProficiencyLevel
{
    /// <summary>
    /// Level 1: Basic awareness and understanding
    /// </summary>
    Beginner = 1,

    /// <summary>
    /// Level 2: Can perform simple tasks with guidance
    /// </summary>
    Elementary = 2,

    /// <summary>
    /// Level 3: Can perform independently with occasional guidance
    /// </summary>
    Intermediate = 3,

    /// <summary>
    /// Level 4: Expert-level proficiency, can mentor others
    /// </summary>
    Advanced = 4,

    /// <summary>
    /// Level 5: Recognized expert, can lead and innovate
    /// </summary>
    Expert = 5
}
